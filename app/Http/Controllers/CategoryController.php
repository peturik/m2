    public function index($slug)
    {
        $category = Category::where('slug', $slug)->first();

        $posts = Post::where('category_id', $category->id)
            ->where('active', 1)
            ->latest()
            ->paginate(5);

        return view('posts.index', compact('posts', 'category'));
    }

